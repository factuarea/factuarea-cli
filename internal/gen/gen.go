package gen

import (
	"bytes"
	"fmt"
	"go/format"
	"text/template"

	"github.com/factuarea/factuarea-cli/internal/spec"
)

func Generate() (src []byte, nonConforming []string, err error) {
	ops, nonConforming, err := spec.Load()
	if err != nil {
		return nil, nil, err
	}
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, ops); err != nil {
		return nil, nonConforming, err
	}
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, nonConforming, fmt.Errorf("gofmt del código generado: %w\n%s", err, buf.String())
	}
	return formatted, nonConforming, nil
}

func hasObjectArray(fields []spec.BodyField) bool {
	for _, f := range fields {
		if f.Kind == "object_array" {
			return true
		}
	}
	return false
}

var tmpl = template.Must(template.New("gen").Funcs(template.FuncMap{
	"q":              func(s string) string { return fmt.Sprintf("%q", s) },
	"hasObjectArray": hasObjectArray,
}).Parse(`// Code generated by internal/gen; DO NOT EDIT.
package cmd

type genParam struct {
	Name, In, Type, Description string
	Required                    bool
}
type genBodyField struct {
	Name, Type, Kind string
	Required, Nullable bool
	Enum             []string
	Children         []genBodyField
}
type genBody struct {
	Kind, Example string
	FileFields    []string
	Fields        []genBodyField
	HasObjectArray bool
}
type genOp struct {
	OperationID, Method, Path, Action, Summary string
	Deprecated                                 bool
	Irreversible                               bool
	RequiredScope                              string
	Groups                                     []string
	PathParams, QueryParams                    []genParam
	Body                                       *genBody
	BinaryContentType                          string
}

func generatedOps() []genOp {
	return []genOp{
{{- range .}}
		{
			OperationID: {{q .OperationID}}, Method: {{q .Method}}, Path: {{q .Path}},
			Action: {{q .Action}}, Summary: {{q .Summary}}, Deprecated: {{.Deprecated}},
			Irreversible: {{.Irreversible}}, RequiredScope: {{q .RequiredScope}},
			Groups: []string{ {{range .Groups}}{{q .}}, {{end}} },
			PathParams: []genParam{ {{range .PathParams}}{Name: {{q .Name}}, In: {{q .In}}, Type: {{q .Type}}, Description: {{q .Description}}, Required: {{.Required}}}, {{end}} },
			QueryParams: []genParam{ {{range .QueryParams}}{Name: {{q .Name}}, In: {{q .In}}, Type: {{q .Type}}, Description: {{q .Description}}, Required: {{.Required}}}, {{end}} },
			{{if .Body}}Body: &genBody{Kind: {{q .Body.Kind}}, Example: {{q .Body.Example}}, FileFields: []string{ {{range .Body.FileFields}}{{q .}}, {{end}} }, HasObjectArray: {{hasObjectArray .Body.Fields}}, Fields: []genBodyField{ {{template "fields" .Body.Fields}} }},{{end}}
			{{if .BinaryResponse}}BinaryContentType: {{q .BinaryResponse.ContentType}},{{end}}
		},
{{- end}}
	}
}
{{define "fields"}}{{range .}}{Name: {{q .Name}}, Type: {{q .Type}}, Kind: {{q .Kind}}, Required: {{.Required}}, Nullable: {{.Nullable}}, Enum: []string{ {{range .Enum}}{{q .}}, {{end}} }, Children: []genBodyField{ {{template "fields" .Children}} }}, {{end}}{{end}}
`))
